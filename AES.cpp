string rotword(string s,int n)
{
   
   for(int i=0;i<2;i++)
   {
       char a=s[0];
       for(int j=0;j<n;j++)
       {
           s[j]=s[j+1];
       }
       s[n-1]=a;
   }
   return s;
}
